#include <stdio.h>
int main(){

/*  escape sequence = character combination of a backslash \ followed by a
    letter or combination of digits. 
    They specify actions within a line ot string of a text.
    \n = newline
    \t = tab
*/

 printf("hi everyone!\n");
 printf("1\t2\t3\t4\t5\n6\t7\t8\t9\t10\n");


 printf("I like Pizza - what you like\n");


 printf("\"I like Pizza\" - what you like\n");


 printf("\'I like Pizza\' - what you like\n");

 printf("\\I like Pizza\\ - what you like\n");

 return 0;   
}